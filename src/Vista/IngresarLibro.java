    private void bt_grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_grabarActionPerformed
        try {
          
            
            boolean prestado = r_si.isSelected();
            
            String mensaje = libroControlador.agregarLibro(prestado, Integer.parseInt(caja_codigo.getText()),
                    Integer.parseInt(caja_annio.getText()),
                    caja_titulo.getText(), caja_autor.getText(),
                    caja_editorial.getText());
            
            JOptionPane.showMessageDialog(this, mensaje);
        } catch (Exception ex) {
           JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
        
        
    }//GEN-LAST:event_bt_grabarActionPerformed

