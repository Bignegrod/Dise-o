    public void buscarUnLibro(int codigo)
    {
        DefaultTableModel modelo = (DefaultTableModel) tablaLibros.getModel();
        modelo.getDataVector().removeAllElements();//limpia la jtable
        Libro unLibro;
        try {
            unLibro = libroControlador.buscarLibro(codigo);
      
        Object filaDatos[] = new Object[6];
        filaDatos[0]=unLibro.getCodigo();
        filaDatos[1]=unLibro.getAnioPublicacion();
        filaDatos[2]=unLibro.getTitulo();
        filaDatos[3]=unLibro.getAutor();
        filaDatos[4]=unLibro.getEditorial();
        filaDatos[5]=unLibro.isPrestado();
        modelo.addRow(filaDatos);
          } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
    }
    
    public void crearTabla()
    {
        
        DefaultTableModel modelo = (DefaultTableModel) tablaLibros.getModel();
        modelo.getDataVector().removeAllElements();//limpia la jtable
        try {
            
            
            ArrayList<Libro> lista = libroControlador.buscarTodoLibro();
            
            Object filaDatos[] = new Object[6];
            
            for(int i=0; i<lista.size(); i++)
            {
                  filaDatos[0]= lista.get(i).getCodigo();
                  filaDatos[1]= lista.get(i).getAnioPublicacion();
                  filaDatos[2] = lista.get(i).getTitulo();
                  filaDatos[3] = lista.get(i).getAutor();
                  filaDatos[4] = lista.get(i).getEditorial();
                  filaDatos[5] = lista.get(i).isPrestado();
                  modelo.addRow(filaDatos);
            
            }
            
            
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
          
    
    
    }

