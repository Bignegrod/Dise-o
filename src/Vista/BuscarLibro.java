    private void btModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btModificarActionPerformed
        // TODO add your handling code here:
        if(tablaLibros.getRowCount()>0)  //rowcount muestra las filsa que hay
        {
            int fila=tablaLibros.getSelectedRow();//retorna el numero de la fila seleccionada
            if(fila >+-1)
            {
                DefaultTableModel modelo= (DefaultTableModel) tablaLibros.getModel();
                int codigo=Integer.parseInt (modelo.getValueAt(fila, 0).toString());
                if(codigo>0)
                {
                    int anio=Integer.parseInt (modelo.getValueAt(fila, 1).toString());
                    String titulo=(modelo.getValueAt(fila, 2).toString());
                    String autor=(modelo.getValueAt(fila, 3).toString());
                    String editorial=(modelo.getValueAt(fila, 4).toString());
                    boolean prestado = Boolean.parseBoolean(modelo.getValueAt(fila, 5).toString());
                    try {
                        String mensaje = libroControlador.modificarLibro(prestado, codigo, anio, titulo, autor, editorial);
                     JOptionPane.showMessageDialog(this, mensaje);
                    }
                    catch(Exception ex)
                    {
                        JOptionPane.showMessageDialog(this, ex.getMessage());
                    }
                    }
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Seleccione una fila");
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "tabla sin datos");
        }
    }//GEN-LAST:event_btModificarActionPerformed

    private void btEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEliminarActionPerformed

        if (tablaLibros.getRowCount() > 0) //rowcount muestra las filsa que hay
        {
            int fila = tablaLibros.getSelectedRow();//retorna el numero de la fila seleccionada
            if (fila > +-1) {
                DefaultTableModel modelo = (DefaultTableModel) tablaLibros.getModel();
                int codigo = Integer.parseInt(modelo.getValueAt(fila, 0).toString());
                if (codigo > 0) 
                {
                    String mensaje= libroControlador.eliminar(codigo);
                    JOptionPane.showMessageDialog(this, "Eliminacion exitosa");
                    crearTabla();
                            
                }

            } else {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }

    }//GEN-LAST:event_btEliminarActionPerformed

